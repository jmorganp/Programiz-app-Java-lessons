class Main {
    public static void main(String[] args) {
        // create strings
        String str1 = "Java ";
        String str2 = "Programming";

        // print two strings
        System.out.println("First String: " + str1);
        System.out.println("Second String: " + str2);

        // join two strings
        String joinedString = str1.concat(str2);
        System.out.println(joinedString);
    }
}