class Main {
    public static void main(String[] args) {
        // create an array of String type
        String[] languages = {"Java", "Python", "C"};

        // print string array 
        System.out.print("Languages: ");

        for (String lang: languages) {
            System.out.print(lang + ", ");
        }
    }
}

// Output: Languages: Java, Python, C,