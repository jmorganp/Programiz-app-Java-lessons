class Main {
    public static void main(String[] args) {
        int a = 7;

        // greater than
        System.out.println(a > 4); // true

        // greater than or equal to 
        System.out.println(a >= 7); // true
        System.out.println(a >= 8); // false

        // less than
        System.out.println(a < 4); // false

        // less than or equal to
        System.out.println(a <= 4); // false
        System.out.println(a <= 7); // true

        // equal to
        System.out.println(a == 4); // false

        // not equal to
        System.out.println(a != 4); // true
    }
}