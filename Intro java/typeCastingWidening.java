// Widening type casting

class Main {
    public static void main(String[] args) {
        int num = 18;
        double sum;
        sum = num + 4.5;

        System.out.println("Sum: " + sum); // 22.5

        // This type of casting is known as widening type casting
        // where smaller size typ eis converted to larger size type.
    }
}