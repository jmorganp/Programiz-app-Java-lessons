class Main {
    public static void main(String[] args) {
        // create in variables
        int n1 = 5, n2 = 4;

        // increase value of n1 by 1
        ++n1;

        // decrease value of n2 by 1
        --n2;

        System.out.println("n1 = " + n1);
        System.out.println("n2 = " + n2);

        /*
         * The ++ and -- operators can only be used with integers.
         * THis operators can also be used as posfixes like n1++ and n2--
         */
    }
}